    private void table_displayLecturerDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_displayLecturerDetailsMouseClicked
        // TODO add your handling code here:
        int i = table_displayLecturerDetails.getSelectedRow();
        TableModel model = table_displayLecturerDetails.getModel();
        
        String lecturer_fullName = model.getValueAt(i, 0).toString();
        String[] arr_nameSplit = lecturer_fullName.split("\\s+");
        lec_firstName = arr_nameSplit[0];
        lec_lastName = arr_nameSplit[1];
        empId_rowSelected = model.getValueAt(i, 1).toString();
        faculty = model.getValueAt(i, 2).toString();
        department = model.getValueAt(i, 3).toString();
        center = model.getValueAt( i, 4).toString();
        building = model.getValueAt(i, 5).toString();
        String level_profession = model.getValueAt(i, 6).toString();
        
        if( level_profession == "Professor"){
            level = 1;
        } else if ( level_profession == "Assistant Professor"){
            level = 2;
        } else if ( level_profession == "Senior Lecturer(HG)"){
            level = 3;
        } else if ( level_profession == "Senior Lecturer"){
            level = 4;
        }else if ( level_profession == "Lecturer"){
            level = 5;
        }else if ( level_profession == "Assistant Lecturer"){
            level = 6;
        }else if ( level_profession == "Instructor"){
            level = 7;
        }
        
    }//GEN-LAST:event_table_displayLecturerDetailsMouseClicked

