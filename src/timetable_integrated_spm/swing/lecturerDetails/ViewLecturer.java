    public ArrayList<Lecturer> lecturerList() {
        
        ArrayList<Lecturer> lecturerList = new ArrayList<>();
        try {    
            String query = "SELECT * FROM lecturer";
            statement = connection.createStatement();
            ResultSet rs = statement.executeQuery(query);
            
            Lecturer lecturer;
            
            while(rs.next()){
                lecturer = new Lecturer( rs.getString("lecturer_fname"), rs.getString("lecturer_lname"), rs.getString("employeeId"), rs.getString("faculty"), 
                                                rs.getString("department"), rs.getString("center"), rs.getString("building"), rs.getInt("level"));
                lecturerList.add(lecturer);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex + "Exception occured in LecturerList method");
            Logger.getLogger(ViewLecturer.class.getName()).log(Level.SEVERE, null, ex); 
        }
        return lecturerList;
    }
    
    public void show_lecturerDetails(){
        ArrayList<Lecturer> lecturerList = lecturerList();
        DefaultTableModel tableModel = (DefaultTableModel) table_displayLecturerDetails.getModel();
        Object[] row = new Object[7];
        for (int i = 0; i < lecturerList.size(); i++) {
            row[0] = lecturerList.get(i).getLecturer_fname() + " " + lecturerList.get(i).getLecturer_lname();
            row[1] = lecturerList.get(i).getEmpId();
            row[2] = lecturerList.get(i).getFaculty();
            row[3] = lecturerList.get(i).getDepartment();
            row[4] = lecturerList.get(i).getCenter();
            row[5] = lecturerList.get(i).getBuilding();
            int level = lecturerList.get(i).getAcademicRank();
            
            if (level == 1 ){
                row[6] = "Professor"; 
            } else if (level == 2 ){
                row[6] = "Assistant Professor"; 
            } else if (level == 3 ){
                row[6] = "Senior Lecturer(HG)"; 
            } else if (level == 4 ){
                row[6] = "Senior Lecturer"; 
            } else if (level == 5 ){
                row[6] = "Lecturer"; 
            } else if (level == 6 ){
                row[6] = "Assistant Lecturer"; 
            } else if (level == 7 ){
                row[6] = "Instructor"; 
            }
            
                    
            tableModel.addRow(row);
        }
    }
    
    private void dbconnect(){
        final String DRIVER = "org.apache.derby.jdbc.EmbeddedDriver";
        final String JDBC_URL = "jdbc:derby:unidb;create=true";
        
        try {
            Class.forName(DRIVER);
            connection = DriverManager.getConnection(JDBC_URL);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddLecturer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddLecturer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }  
        
    }

