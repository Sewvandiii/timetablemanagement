    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int opt = JOptionPane.showConfirmDialog(null, "Are you sure you want to Delete " + lec_firstName + " "
                + lec_lastName + " details ?", "Delete", JOptionPane.YES_NO_OPTION);

        if (opt == 0) {
            try {
                String query = "DELETE FROM lecturer WHERE employeeId = ?";

                preparedStatement = connection.prepareStatement(query);
                preparedStatement.setString(1, empId_rowSelected);

                // execute the preparedstatement
                preparedStatement.execute();

                DefaultTableModel model = (DefaultTableModel) table_displayLecturerDetails.getModel();
                model.setRowCount(0);
                show_lecturerDetails();


                JOptionPane.showMessageDialog(null, "Record deleted successfully from the Database");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Something went wrong! Please try again.");
                System.err.println("Exception in delete operation : " + ex);
                System.err.println(ex.getMessage());
                Logger.getLogger(ViewLecturer.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

